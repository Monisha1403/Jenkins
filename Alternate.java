class Alternate{
    public static void main(String[] args){
        int count=0;
        for(int n=2;n<=50;n++){
            int c=0;
            for(int i=1;i<=n;i++){
                if(n%i == 0)
                  c++;
            }
            if(c==2){
                count++;
                if(count%2 == 1)
                  System.out.print(n + " ");
            }
        }
    }
}
